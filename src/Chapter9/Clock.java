package Chapter9;
import java.time.*;

public class WClock {
    private int hours;
    private int minutes;

    public void setHours(int hours)  {
        this.hours = hours;
    }

    public void setMinutes(int minutes) {
        this.minutes = minutes;
    }
    public int getHours() {
        return hours;
    }

    public int getMinutes() {
        return minutes;
    }

}
